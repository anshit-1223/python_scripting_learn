#for loop in python
fruits=['Apple','Banana','Orange','Grapes','Mango']
for x in fruits:
    print(x)

#looping through string
for x in "hello":
    print(x)

 # range function
for x in range(6):
    print(x)  

#else in for loop
for x in range(6):
    print(x)  
else:
    print("Loop Finished!!")

